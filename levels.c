void reset_level()
{
     int i, j;
     for (i = 0; i < ROWS; i++)
     {
          for (j = 0; j < COLS; j++)
          {
               if (selected_level[i][j][1] == 1)
               {
                    selected_level[i][j][0] = 0;
               }
          }
     }
}

void set_level(char selection)
{
     switch (selection)
     {
     case 'e':
          selected_level = easy;
          current_level = EASY;
          break;
     case 'm':
          selected_level = meduim;
          current_level = MEDUIM;
          break;
     default:
          selected_level = hard;
          current_level = HARD;
          break;
     }
}

char get_currentSelectedLevel(){
     return current_level;
}

int get_level_emptyPlaces_count()
{
     int i, j, count = 0;
     for (i = 0; i < ROWS; i++)
     {
          for (j = 0; j < COLS; j++)
          {
               if (selected_level[i][j][0] == 0)
               {
                    count++;
               }
          }
     }
     return count;
}